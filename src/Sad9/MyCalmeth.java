    private void beqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beqlActionPerformed
      double ans = 0 , sno = Double.parseDouble(txt.getText());
      if(op.equals("+"))
      {
          ans = fno+sno;
          
      }
      else if(op.equals("-"))
      {
          ans = fno - sno;
      }
      else if(op.equals("*"))
      {
          ans = fno * sno;
      }
      else if(op.equals("/"))
      {
          ans = fno / sno;
      }
      txt.setText(""+ans);
    }//GEN-LAST:event_beqlActionPerformed

